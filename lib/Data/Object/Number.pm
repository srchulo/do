sub abs {
  my ($self, @args) = @_;

  try {
    my $func = 'Data::Object::Func::Number::Abs';

    return cast(load($func)->new($self, @args)->execute);
  }
  catch {
    my $error = $_;

    $self->throw(ref($error) ? $error->message : "$error");
  };
}

